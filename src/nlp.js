const day_length = 12

function find_instructions(package_text) {
    package_text = package_text.toLowerCase();
    const start = package_text.indexOf("take");
    return package_text.slice(start);
}
function isNumeric(str) {
    if (typeof str != "string") return false // we only process strings!  
    return !isNaN(str) && // use type coercion to parse the _entirety_ of the string (`parseFloat` alone does not do this)...
        !isNaN(parseFloat(str)) // ...and ensure strings of whitespace fail
}

function tokenize(text) {
    text = text.toLowerCase();
    // convert 'one' to '1`, 'two' to '2', etc.
    for (const [key, value] of numbers.entries()) {
        text = text.replaceAll(key, value);
    }
    // tokenize
    return text.split(" ").map(x => isNumeric(x) ? parseInt(x) : x);
}

// Tablet bottles normally will have
// 'per day', 'daily', etc
// This finds how many we need to take per day
function find_interval(text) {
    text = find_instructions(text);
    let tokenized = tokenize(text);
    for (let i = 0; i < tokenized.length; i++) {
        if (intervals.has(tokenized[i])) {
            // loop backwards to find the nearest number
            let interval = 1;
            for (let j = i - 1; j >= 0 && j >= i - 4; j--) {
                if (tablet_synonyms.has(tokenized[j])) {
                    break
                }
                if (typeof (tokenized[j]) == "number") {
                    interval = tokenized[j]
                }
            }
            // If this is a 'per hour' divide by 24 to find per day
            if (hourly_intervals.has(tokenized[i])) {
                return day_length / interval
            }
            else {
                return interval
            }
        }
    }
}

function find_tablet_count(text) {
    text = find_instructions(text);
    let tokenized = tokenize(text);
    for (let i = 0; i < tokenized.length; i++) {
        if (tablet_synonyms.has(tokenized[i])) {
            // loop backwards to find the nearest number
            let interval = 1;
            for (let j = i - 1; j >= 0 && j >= i - 4; j--) {
                if (typeof (tokenized[j]) == "number") {
                    return tokenized[j]
                }
            }
        }
    }
    return 1;
}

function find_drug_name(text) {
    let tokenized = tokenize(text);
    // Find all the words that are in drug list.
    // If there are multiple return the longest one
    let wordlist = new Set(tokenized)
    // take the intersectino of wordlist and druglist
    let intersection = new Set([...wordlist].filter(x => drugs.has(x)));
    // return the longest word in intersection
    let result = new Array(intersection).reduce((c, v) => c.length > v.length ? c : v);
    if (result.size != 0 && !result.has("vitamin")) {
        return result.values().next().value
    }
    // Add all the vitamins to result
    // loop through all the tokens
    for (let i = 0; i < tokenized.length; i++) {
        // A vitamin is the word 'vitamin' followed by a 1 or 2 letter word
        if (i < tokenized.length - 1 && tokenized[i] == "vitamin" && tokenized[i + 1].length <= 2) {
            return "vitamin " + tokenized[i + 1]
        }
    }
    return ""
}


function activate_nlp(text) {
    let drug = find_drug_name(text);
    let interval = find_interval(text);
    let tablet_count = find_tablet_count(text);
    const values = [drug, interval, tablet_count]
    return values
}

